//! The widths of standard fonts

/// Return *editor font widths* (i.e. advance) for a well-known font name
pub fn standard_widths(cset: &str) -> Option<&[u8; 128]> {
    match cset {
        "NORMANDE" => Some(&NORMANDE),
        "PINSEL" => Some(&PINSEL),
        "GROTMIKR" => Some(&GROTMIKR),
        "ANTIKRO" => Some(&ANTIKRO),
        _ => None,
    }
}

